function createDOM(arr) {
    // hire.innerHTML = null;

    arr.forEach((el,ind) => {
        let div = document.createElement("hiring");
        div.setAttribute("class","hire");
        div.setAttribute("onclick","location.href=`./searchPage.html`")
        let h3 = document.createElement("h3");
        let p = document.createElement("p");
        let img1 = document.createElement("img");
        img1.setAttribute("src", el.img1);
        let img2 = document.createElement("img");
        img2.setAttribute("src", el.img2);
        let img3 = document.createElement("img");
        img3.setAttribute("src", el.img3);
        let img4 = document.createElement("img");
        img4.setAttribute("src", el.img4);

        div.style.border = "1px solid #EEEEEE"
        div.style.borderRadius = "30px 10px"
        div.style.padding = "5px 0px 0px 40px"
        p.style.color = "#445578"

        h3.innerText = el.name;
        p.innerText = el.desc;

        div.append(h3,p,img1,img2,img3,img4);
        hire.append(div);

    });
};

function sponsors(arr) {
    spons.innerHTML = null;

    arr.forEach((el,ind) => {
        let bigDiv = document.createElement("div");
        bigDiv.setAttribute("class", "sponserHov");
        bigDiv.setAttribute("onclick","location.href=`./searchPage.html`");
        let img = document.createElement("img");
        img.setAttribute("src", el.img);
        let h3 = document.createElement("h3");
        let span1 = document.createElement("span");
        let span2 = document.createElement("span");
        let div = document.createElement("div");
        let ratImg = document.createElement("img");
        ratImg.setAttribute("src", "//static.naukimg.com/s/7/0/assets/images/node_modules/@naukri-ui-dev/premiumstandardads/component/assets/star.0f830ab5.svg")
        
        h3.style.margin = "10px"
        h3.innerText = el.name;
        span1.innerText = el.rating;
        span2.innerText = el.cnt;
    
        for(let i = 0; i < el.type.length; i++){
            let span = document.createElement("span");
            span.style.color = "#445578";

            let div2 = document.createElement("div");
            div2.style.border = "1px solid #ECF5FF"

            span.innerText = el.type[i];

            div2.append(span);
            div2.style.margin = "9px 0px 0px 0px"
            div.append(div2);
        }
        let line = document.createElement("span");
        line.innerText = "   |   ";
        div.style.padding = "5px 10px 10px 10px"
        
        // div.style.border = "1px solid #EEEEEE"
        div.style.display = "flex";
        div.style.flexWrap = "wrap";
        div.style.justifyContent = "space-around"

        bigDiv.append(img,h3,ratImg,span1,line,span2,div)
        spons.append(bigDiv);
    });
};

